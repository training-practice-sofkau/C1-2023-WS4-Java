package adapter;

public class Keyboard {

    public void keyUp(){
        System.out.print("↑");
    }

    public void keyDown(){
        System.out.print("↓");
    }

    public void keyLeft(){
        System.out.print("←");
    }

    public void keyRight(){
        System.out.print("→");
    }

    public void click(){
        System.out.print("Action");
    }

    public void spaceBar(){
        System.out.print("Jump");
    }
}
